<script setup>
import { computed } from 'vue';
import { Link } from '@inertiajs/inertia-vue3';

const props = defineProps({
    href: String,
    active: Boolean,
});

const classes = computed(() => {
    return props.active
        ? 'inline-flex items-center py-1 rounded-full px-2 bg-cyan-500 hover:bg-cyan-400 shadow text-white text-sm font-medium leading-5 focus:outline-none focus:border-indigo-700 transition'
        : 'inline-flex items-center py-1 rounded-full px-2 text-sm font-medium leading-5 text-gray-500 hover:text-gray-700 hover:border-gray-300 focus:outline-none focus:text-gray-700 focus:border-gray-300 transition';
});
</script>

<template>
    <Link :href="href" :class="classes">
        <slot />
    </Link>
</template>
